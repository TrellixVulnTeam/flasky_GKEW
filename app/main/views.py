# 12.4 查询所有文章还是所关注用户的文章
@main.route('/all')
@login_required
def show_all():
    # resp 为空，代表查询所有博客
    resp = make_response(redirect(url_for('.index')))
    resp.set_cookie('show_followed', '', max_age=30*24*60*60)  # 30天
    return resp


@main.route('/followed')
@login_required
def show_followed():
    # resp 为1不空，代表只查询关注用户的博客
    resp = make_response(redirect(url_for('.index')))
    resp.set_cookie('show_followed', '1', max_age=30*24*60*60)
    return resp


